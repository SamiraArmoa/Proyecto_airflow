BEGIN TRANSACTION;

-- 1. TABLA DE USUARIOS (Unificada para Admin, Empleado y Pasajero)
DROP TABLE IF EXISTS "Usuario";
CREATE TABLE "Usuario" (
	"ID_US"	INTEGER,
	"NOMBRE"	TEXT NOT NULL,
	"EMAIL"	TEXT UNIQUE NOT NULL,
	"TLF"	INTEGER,
	"CONTRASENA"	TEXT NOT NULL,
	"ROL"	INTEGER NOT NULL, -- 0: Admin, 1: Empleado, 2: Pasajero
	PRIMARY KEY("ID_US" AUTOINCREMENT)
);

-- 2. TABLA DE AEROPUERTOS (Datos maestros desde CSV)
DROP TABLE IF EXISTS "Aeropuerto";
CREATE TABLE "Aeropuerto" (
	"ID_A"	INTEGER,
	"CODIGO"	TEXT UNIQUE NOT NULL, -- Ej: BIO, MAD, JFK
	"NOMBRE"	TEXT,
	"CIUDAD"	TEXT,
	PRIMARY KEY("ID_A" AUTOINCREMENT)
);

-- 3. TABLA DE VUELOS (Conexión 1-N con Aeropuertos)
DROP TABLE IF EXISTS "Vuelo";
CREATE TABLE "Vuelo" (
	"ID_VUELO"	INTEGER,
	"COD_VUELO"	TEXT NOT NULL, -- Ej: AF123
	"ID_ORIGEN"	INTEGER,
	"ID_DESTINO"	INTEGER,
	"FECHA_HORA" TEXT,
	"PRECIO"	REAL,
	"CAPACIDAD" INTEGER,
	PRIMARY KEY("ID_VUELO" AUTOINCREMENT),
	FOREIGN KEY("ID_ORIGEN") REFERENCES "Aeropuerto"("ID_A") ON DELETE CASCADE,
	FOREIGN KEY("ID_DESTINO") REFERENCES "Aeropuerto"("ID_A") ON DELETE CASCADE
);

-- 4. TABLA DE BILLETES (Tabla intermedia N-M entre Usuario y Vuelo)
DROP TABLE IF EXISTS "Billete";
CREATE TABLE "Billete" (
	"ID_BILLETE" INTEGER,
	"ID_USUARIO" INTEGER,
	"ID_VUELO"	 INTEGER,
	"ASIENTO"	 TEXT,
	"FECHA_COMPRA" TEXT,
	PRIMARY KEY("ID_BILLETE" AUTOINCREMENT),
	FOREIGN KEY("ID_USUARIO") REFERENCES "Usuario"("ID_US") ON DELETE CASCADE,
	FOREIGN KEY("ID_VUELO") REFERENCES "Vuelo"("ID_VUELO") ON DELETE CASCADE
);

-- 5. TABLA DE EQUIPAJES (Relación N-1 con Billete)
DROP TABLE IF EXISTS "Equipaje";
CREATE TABLE "Equipaje" (
	"ID_EQUIPAJE" INTEGER,
	"ID_BILLETE"  INTEGER,
	"TIPO"	      TEXT, -- Mano, Facturado, Especial
	"PESO"	      REAL,
	"DESCRIPCION" TEXT,
	PRIMARY KEY("ID_EQUIPAJE" AUTOINCREMENT),
	FOREIGN KEY("ID_BILLETE") REFERENCES "Billete"("ID_BILLETE") ON DELETE CASCADE
);

COMMIT;